/**
 * Grabbing é o estado em que o utilizador ter o rato ou dedo sobre a orb.
 * Holding é o estado em que o utilizador arrasta a orb pelo ecrâ.
 * Releasing é o estado em que o utilizador larga a orb.
 * None é o estado em que o utilizador não está a executar nenhuma das ações acimas referidas.
 */
public enum InputStatus
{
    Grabbing,
    Holding,
    Releasing,
    None
}